import math

# Ceil
a = 2.3
ceil_a = math.ceil(a)
print(f"ceil of {a} is {ceil_a}")

# Floor
floor_a = math.floor(a)
print(f"floor of {a} is {floor_a}")

# Factorial
a = 5
factorial_a = math.factorial(a)
print(f"factorial of {a} is {factorial_a}")

# Power
value = 2**3
print(f"value of 2^3 is {value}")

# Square root
a = 16
square_root_a = math.sqrt(a)
print(f"square root of {a} is {square_root_a}")
